//(Display a pattern) Write a program that displays the following pattern:
/*
    J
J  aaa v vaaa
J  J aa v v a a
J   aaaa v aaaa 
 */
public class Ex1_3 {
    public static void main(String[] args) {
        System.out.println("    J");
        System.out.println("J  aaa\t  v\tvaaa");
        System.out.println("J  J  aa    v v\t   a a");
        System.out.println("J   aaaa     v\t   aaaa");
        
    }
}
